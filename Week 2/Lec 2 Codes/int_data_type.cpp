#include<iostream>
using namespace std;

int main() {
	int age = 25;                               // signed in (default)
	unsigned int distance = 100;               //  usigned int
	short int temperature = -10;              //   short int
	unsigned short int count = 500;          //    unsigned short int
	long int population = 900000000;        //     long int
	unsigned long int bigNum = 1000000000; //      unsigned long int
	
	cout<<"Age: "<<age <<endl;
	cout<<"Distance: "<<distance <<endl;
	cout<<"Temperature: "<<temperature <<endl;
	cout<<"Count: "<<count <<endl;
	cout<<"Population: "<<population <<endl;
	cout<<"Big NUmber: "<<bigNum <<endl;
	
	return 0;
}
