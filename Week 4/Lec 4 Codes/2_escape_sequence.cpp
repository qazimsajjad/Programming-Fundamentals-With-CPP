#include<iostream>
using namespace std;

int main() {
	
	cout<<"This program will Explain Escape Sequence\n";
	cout<<"\'\\n\':\n";
	cout<<"I Love Pakistan\n";
	cout<<"I\nLove Pakistan";
	cout<<"\nI \nLove \nPakistan\n\n";
	
	cout<<"\'\\t\':\n";
	cout<<"1\t2\t3\t4";
	
	cout<<"\n\n";
	
	// if you want to print your name in double/single qoutes
	// if you don't use \" compiler will throw error
	cout<<"Hi, I am \"[Your Name]\"";

	
	return 0;
}
